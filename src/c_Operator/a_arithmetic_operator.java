public class a_arithmetic_operator {
	public static void main(String[] args) {
		int num1 = 8, num2 = 4;
		
        System.out.println("num1 : "+num1+", num1 : "+num2);
		System.out.println("+ 연산자에 의한 결과 : "+ (num1 + num2));
		System.out.println("- 연산자에 의한 결과 : "+ (num1 - num2));
		System.out.println("* 연산자에 의한 결과 : "+ (num1 * num2));
		System.out.println("/ 연산자에 의한 결과 : "+ (num1 / num2));
		System.out.println("% 연산자에 의한 결과 : "+ (num1 % num2));
	}
}